import numpy as np 

array1 = np.array([[1, 2, 3], [4, 5, 6]])
array2 = np.array([2, 4, 6])

product_array = np.multiply(array1, array2)
print(product_array)

power_array = np.power(array1, array2)
print(power_array)

divided_array = np.divide(array1, array2)
print(divided_array)

add_array = np.add(array1, array2)
print(add_array)